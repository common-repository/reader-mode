    <section class="section-reader-mode section-full">
        <div class="col-description">
            <h2>Reader Mode Layout</h2>
            <p>
                Reader Mode adds a simple and clean layout for better readability and accessibility for the users on
                every post/ article by stripping away clutter and unnecessary elements from the post/ article.
            </p>

            <p>
                To enable the reader mode layout for the users you need to enable the Reader Mode button in the <code>Reader
                    Mode > Settings > General Settings</code> page.
            </p>

            <p>
                After enabling the Reader Mode button, the Reader Mode button will be displayed in the post/ article
                based on the display options you have set in the <code>Reader Mode > Settings > Display Settings</code>
                page.
            </p>
            <p>
                From the Display Settings page, you can select the post types where you want to display the Reader Mode
                button, and also you can select the display position for the Reader Mode button.
            </p>

            <p>
                You can also customize the Reader Mode button with different layouts and colors from the <code>Reader
                    Mode > Settings > Button Settings</code> page.
            </p>
            <p>
                When the user clicks on the Reader Mode button, the post/ article will be displayed in the reader mode
                layout.
            </p>
            <p>
                On the Reader Mode layout users can customize the Reader Mode layout appearance as they want and also
                use the Text to Speech, Translation, Print, Fullscreen, Reading Progress, and Auto-scroll features
            </p>
        </div>

